#include "libft.h"

char	*ft_strcat(char *dest, char *src)
{
	size_t	dest_len;
	int		i;

	dest_len = ft_strlen(dest);
	i = 0;
	while (src[i])
	{
		dest[dest_len + i] = src[i];
		i++;
	}
	dest[dest_len + i] = '\0';
	return (dest);
}
